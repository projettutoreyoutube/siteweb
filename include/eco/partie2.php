    L'offre "YouTube Red"
  </div>

  <div class="content">
    YouTube Red, récemment amélioré en “YouTube Premium” permet d'accéder à divers avantages sur la version web et sur téléphone tels que:<br>
      <div class="content">
        ●	L’accès à tout YouTube est ses différentes branches sans publicité.<br>
        ●	La possibilité de télécharger des vidéos pour les regarder hors connexion.<br>
        ●	La possibilité de lire des vidéos en arrière-plan ou lorsque le téléphone est verrouillé, parfait pour l’écoute de musique.<br>
        ●	Un accès complet à Google Play Musique.<br>
        ●	Un accès aux séries et longs métrages “YouTube Originals” exclusifs à cet   abonnement.
      </div>
  </div>

  <div class="main_subtitle">
    La fonctionnalité "Rejoindre"
  </div>

  <div class="content">
    Depuis peu, YouTube a ajouté la fonctionnalité “Rejoindre” pour les chaînes YouTube éligibles. Cette fonction permet de soutenir son Youtubeur favori à la manière des abonnements Twitch ou Patreon avec des paiements mensuels.        Cette souscription permet d'acheter des badges publics et des émoji exclusifs dans l’espace commentaires, ainsi que d'accéder aux avantages ponctuellement offerts par les créateurs sur le site et l'application YouTube principaux.
    <br>
    La souscription coûte en général autour de 5€ par mois ou plus, évidemment cette fonction est optionnelle et n’est pas disponible à tous les Youtubeurs: il faut que le contenu de la chaîne en question soit convenable par rapport à la charte de YouTube et qu’il soit approuvé par l’équipe de l’entreprise.
  </div>

  <div class="main_subtitle">
    Le "Superchat"
  </div>

  <div class="content">
    «Super Chat est une nouvelle fonctionnalité qui permet aux fans et aux créateurs d'interagir pendant les diffusions en direct.» <br>
    Une autre façon de gagner de l’argent sur YouTube: le Super Chat. Cette fonction permet de mettre en avant un message pendant un intervalle de temps qui varie en fonction de la somme donnée au youtubeur lorsqu’il nous propose une diffusion en direct. Cela est encore une source de revenus pour YouTube: les frais du Super Chat s’élèvent à 30% du don de l’internaute, qui, la plupart du temps, n’est pas au courant que seuls 70% de son argent ira supporter son créateur de contenu favori.
  </div>

  <div class="content">
    Le Super Chat a très récemment subi une “refonte” en raison d’une polémique menée par le  Wall Street Journal en raison de “Super Chats offensifs” à caractère racistes ou autres. Le journal « The Wall Street Journal » a fait pression sur YouTube pour qu'il annule plus de 26 000 dollars de dons, retirant ainsi 70% de cet argent aux youtubeurs qui se retrouvent “punis” car  un de leurs spectateurs à envoyer un message offensant. A partir de maintenant, la plateforme ne partage plus les dons de Super Chats offensifs avec le créateur en question. Désormais, YouTube verse la totalité du montant à l'organisme de charité de son choix, pour éviter toute polémique.
  </div>

