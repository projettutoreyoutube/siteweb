<h4 class="nom">Remarques de Quentin Delignou</h4>
<p class="col-sm-12 paragraph">
	Le rendu du site est joli, pas aussi incroyable que ce que j'avais imaginé au départ, mais selon moi bien réussi dans la mesure où on a seulement fait 4 mois de développement web. Je suis donc très satisfait du travail réalisé par l'équipe.
	<br /><br />
	<strong>Difficultés rencontrés :</strong>
	<li class="paragraph">Positionnement de la barre de navigation lors du scrollage : le but de la barre de navigation est qu'elle reste en place en haut de l'écran lorsque l'utilisateur descend la page. Le problème est que, quand j'ai cherché comment faire, je n'ai trouvé que des solutions utilisant des scripts javascripts, notamment la fonction affix. Comme je ne maitrise pas le javascript, je n'ai pas réussi à l'utiliser. Mr Trannoy m'a orienté sur la propriété css "sticky" pour la position de la barre, et elle reste maintenant en haut l'écran pendant le scrolling (même si l'utilisation de sticky oblige à changer le code d'une manière un peu moins propre, et le positionnement n'est pas parfait).</li>
	<li class="paragraph">Animation lors du retour en haut de la page : mon but était de faire un bouton "retour en haut" avec une animation de scroll inversé lors du retour en haut. Le problème est que ce bouton fonctionne avec des scripts javascript que je ne maitrise pas. J'ai bien tenté un copié/collé des codes d'exemple mais cela n'a rien donné.</li>
	<li class="paragraph">Page de contact : pour la page de contact, le but était de faire un formulaire d'envoi de mail fonctionnel, pas juste une interface graphique. La tâche s'est révélée assez ardue. J'ai commencé par regarder des codes php à écrire pour réaliser ce type de formulaire (des codes avec 300 lignes de php), on m'a ensuite parlé de l'application PHP Mailer que j'ai essayé de mettre en place.</li>
</p>

