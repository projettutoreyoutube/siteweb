		<h4 class="nom">Il était une fois : le rapport de projet tutoré</h4>
		<p class="paragraph">
		L'histoire commence le 21 septembre 2018. Ce jour la, madame Paterlini annonça aux élèves de S1 qu'ils auraient un rapport à rendre pour la fin du semestre. Ni une ni deux, le groupe de notre histoire se créa pour mener à bien la réalisation de ce projet. Ce groupe était au départ composé de 5 valeureux membres : Quentin Delignou, Augustin Desombres, Tom Jacob et Erwan Vinot, menés par le chef de projet Loïc Bertrand. Malheureusement, Augustin ne tint pas la distance, et ils furent forcés de l'abandonner pour poursuivre leur projet.<br>
		Pendant trois semaines, les membres du groupes débattirent du sujet à traiter, et ils finirent par se mettre d'accord : ce serait YouTube et le métier de youtubeur. Ils mirent en place les modalités de réalisation de leur projet.<br>
		<br>
		Le 31 octobre, jour d'Halloween, ils commencèrent la rédaction de leur rapport. Ils eurent deux semaines pour préparer leur plan, ainsi que les principales idées de la partie économie/droit du développement du rapport. Le 15 novembre, date de fin de ces deux semaines, ils envoyèrent ce premier travail à madame Paterlini.<br>
		Dans les deux semaines qui suivirent, ils rédigèrent l'entièreté de leur rapport, qu'ils durent rendre pour le 7 décembre. La rédaction de ce rapport les mis dans une dynamique de travail constructive, et il en résultat une oeuvre complète de 79 pages.<br>
		<br>
		Nos braves compagnons eurent aussi un site internet en lien avec leur sujet à réaliser. Le codage de ce site débuta fin novembre, et se finit le 9 décembre. Ils eurent certaines difficultés à mettre en place leurs idées, mais ils finirent par en triompher et à développer leur site. Lors de la mise en place du contenu de leur rapport sur leur site, ils se rendirent compte horrifiés qu'ils avaient oublié quelques fautes dans le texte du rapport. Fautes qui ne pouvaient pas être détectées par un correcteur linguistiques, et qui leur avaient échappées lors de leur relecture. Ne pouvant plus corriger leur rapport déjà rendu, ils prirent la décision de corriger ces fautes sur leur site.
