    L’algorithme de choix des contenus, une « Business Machine » ?
  </div>

  <div class="content">
    L’onglet “Tendances” et la fonction “Vidéos recommandées pour vous, sont régies par un algorithme de choix des contenus, afin de normalement, proposer du contenu cohérent à l’utilisateur. (surtout pour la fonction “Vidéos recommandées”) On parle de “Business Machine” car il y a souvent des polémiques vis-à-vis de l’onglet “Tendances”, à propos du contenu mis en avant. En effet, le contenu mis en avant est très souvent aguicheur et ne représente pas forcément le contenu sur YouTube en général. Ce phénomène, n’est pas uniquement présent sur YouTube, on le retrouve, par exemple, dans les journaux : lorsque les personnes voient un titre aguicheur, ils sont plus tentés de voir le contenu dudit journal. C’est pareil pour les vidéos YouTube, une pratique appelée “Clickbait”.
