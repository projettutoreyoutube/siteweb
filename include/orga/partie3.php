  <div class="main_subtitle">
    Origine des capitaux
  </div>
  <div class="content">
    Le capital social d’une entreprise est défini par le montant total des ressources en biens et argent apportés par les associés ou les actionnaires.<br>
    Les capitaux de YouTube proviennent de sa société mère Google. Cette dernière est financée par des capitaux d’origine privés.<br>
    Les fonds propres de YouTube LLC s’élèvent à 6,7 millions de dollars américains.
  </div>

  <div class="main_subtitle">
    Nationalité
  </div>
  <div class="content">
    YouTube est une entreprise américaine. Son siège social se trouve à San Bruno en Californie.
  </div>

  <div class="main_subtitle">
    Cotation en bourse
  </div>
  <div class="content">
    YouTube a été racheté par Google en 2006. YouTube est, depuis cette date, une filiale de la société Google. Cette dernière est devenue une filiale de la société Alphabet en 2015.<br>
    En 2014, l’indice du cours des actions Google s’élevait à 500 dollars américains. Depuis 2015 et la création de la société Alphabet qui est un agglomérat de sociétés dont Google fait partie, cet indice a doublé. Il s’élève actuellement à 1030 dollars américains.

