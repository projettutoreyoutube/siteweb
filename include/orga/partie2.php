  <div class="main_subtitle">
    Définition du terme "juridique"
  </div>
  <div class="content">
    Le mot « Juridique » est un adjectif qui qualifie tout ce qui peut avoir un rapport avec le Droit. C’est-à-dire, par exemple, des études juridiques, un conseil juridique, une revue juridique, une institution juridique ou bien une pratique juridique.
  </div>

  <div class="main_subtitle">
    Statut juridique
  </div>

  <div class="content">
    Le statut juridique est défini comme étant un ensemble de textes juridiques qui règlent la situation d’un groupe d’individus, leurs droits, leurs obligations. Il sert à distinguer les indépendants des autres catégories d’actifs qui sont essentiellement composés de salariés. En France, les entreprises peuvent adopter différents statuts comme
    <span title="Entreprise Unipersonnelle à Responsabilité Limitée"><strong>EURL</strong></span>, <span title="Société à Responsabilité Limitée"><strong>SARL</strong></span>, <span title="Société par Actions Simplifiée"><strong>SAS</strong></span> ou <span title="Société Anonyme"><strong>SA</strong></span>.

  <div class="content">
    <strong>Définition d’une société à responsabilité limitée</strong><br>
    Une SARL est définie comme étant une société commerciale. C’est la forme de société la plus simple. Ses principales caractéristiques sont les suivantes :
    <div class="content">
      •	C’est une société composée d’au moins 2 associés et au maximum de 100.<br>
      •	Son capital doit s’élever au minimum à 1€.<br>
      •	La responsabilité de chaque associé est limitée à ses apports. Cela signifie qu’en cas de dettes, la responsabilité des associés ne peut être engagée que pour la valeur de son apport au capital.<br>
      •	Une assemblée générale doit être organisée chaque année.<br>
      •	Le gérant est responsable pénalement et civilement de ses fautes de gestion.
    </div>
  </div>

  <div class="content">
    <strong>YouTube et son statut de Limited Liability Company</strong><br>
    YouTube a le statut de LLC – Limited Liability Company. Ce statut figure parmi les trois principaux statuts juridiques qui existent aux États-Unis, à savoir : SP – Sole Proprietorship, LLC – Limited Liability Company et Corporation.<br>
    Le statut de la LLC est très semblable à celui de la SARL. Pour les deux formes juridiques, la responsabilité de chaque associé est limitée à ses apports.
  </div>

