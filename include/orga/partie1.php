  <div class="main_subtitle">
    Chiffre d'affaires
  </div>
  <div class="content">
    Après avoir effectué pléthore de recherches, nous pouvons conclure qu’il est très difficile d’obtenir des chiffres précis sur le chiffre d’affaires de YouTube. Les rares données disponibles, sont des estimations de la part d’analystes ou autre. Néanmoins, en étudiant les estimations trouvées et en les mettant en commun, une chose est sûre, le chiffre d’affaires de YouTube est en net augmentation depuis sa création
  </div>
  <div class="content">
    Les sponsorings sur YouTube sont de loin le type de publicité le plus efficace: le fait de voir quelqu’un l’utiliser à notre place de façon crédible nous donne assez d’information sur l'intérêt de l’achat du produit du sponsor.
