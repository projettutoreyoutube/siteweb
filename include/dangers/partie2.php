  <div class="main_subtitle">Droit relatif au travail des enfants
  </div>
  <div class="content">
    Le travail des enfants est une chose qui existe depuis l’antiquité et dans toutes les cultures.<br>
    Selon l’Organisation Internationale du Travail, en 2013, il y avait 168 millions d’enfants travailleurs dans le monde, dont environ 85 millions qui auraient un travail dangereux.<br>
    <br>
    En France, le travail des enfants est interdit par décret. Dans d’autres temps, les enfants étaient obligés de travailler pour aider financièrement leurs parents, qui avaient du mal à subvenir seuls aux besoins du foyer.<br>
    Depuis le 6 janvier 1959, l’école est obligatoire jusqu’à 16 ans. Pour remplacer les revenus apportés par les enfants, l’Etat a mis en place un système d’aide sociale aux familles, aujourd’hui connue sous le nom d’allocations familiales.<br>
    <br>
    Grâce à ces mesures, les entreprises ne peuvent légalement pas employer des enfants de moins de 16 ans.

  </div>

  <div class="main_subtitle">Quel rapport avec YouTube ?
  </div>
  <div class="content">
    Cette partie trouve sa place dans le cadre où l’activité de youtubeur est reconnue comme un métier à part entière (nous avons vu tout cela plus en détail dans la partie correspondante). Nous rencontrons 2 problèmes avec le travail de mineurs sur YouTube.<br>
<br>
    <strong>Youtubeur travailleur mineur ?</strong><br>
    Nous savons également que l’âge requis pour créer une chaîne YouTube n’est que de 13 ans. Là est le problème : comment peut-il être reconnu légal de pratiquer un métier à 13 ans alors que la même loi interdit à un enfant de travailler.<br>
    De plus, YouTube ne met en place aucun système de sécurité permettant de prouver que l’utilisateur a bien 13 ans.<br>
    Nous nous retrouvons alors devant un cas de travail illicite potentiel.<br>
    <br>
    <strong>Le rôle des parents : </strong><br>
    Lors de la mise en avant publique des enfants à grande échelle, il est impossible d’imaginer que les parents ne soient pas au courant. Au contraire, sur la grande majorité des vidéos montrant des mineurs, les parents ne sont pas loin.<br>
    Nous pouvons alors nous demander : pourquoi, en tant que parents, veulent-ils que leur enfants soient exposés au grand public, avec toutes les répercussions que cela peut avoir ?<br>
    <br>
    Plusieurs hypothèses sont possibles. La plus gentille serait de dire que ces parents sont simplement fiers de leurs enfants et souhaitent partager leurs quotidiens avec d’autres personnes.
    Une hypothèse plus sombre est que les parents sont attirés par l’argent que peut générer une chaine YouTube, et la facilité de création de contenu mettant en avant des enfants. De nombreuses vidéos « enquête », parfois même accusatrices, ont été réalisées sur le sujet, par exemple les vidéos du Roi des Rats. Ces vidéos mettent en avant le fait que ces enfants ne touchent pas les revenus générés par leur vidéos (étant mineurs, ils ne peuvent légalement pas déclarer de revenus, tout l’argent reviens donc aux parents), qu’ils seraient « exploités » par leur parents qui voient en eux une source de revenus et que les enfants n’auraient aucune idées de cette manigance (étant jeunes, ils n’ont pas le recul nécessaire pour s’en rendre compte). Le Roi des Rats met également en avant le fait que les enfants ne s’expriment jamais publiquement en dehors des vidéos. Toute la communication est gérée par les parents, de mêmes que les vidéos « réponse » aux polémiques.<br>
    <br>
    Ces vidéos sont particulièrement accusatrices, et même s’il est possible de se poser la question de l’exploitation des enfants par les parents, il est également possible de ne pas trouver cela choquant, la forme pouvant être comprise comme une protection des enfants.<br>
    En revanche, il est légitime d’avoir des interrogations sur les revenus générés par des enfants. Légalement, les enfants ne peuvent pas travailler, or leur activité de youtubeur s’y apparente très fortement.<br>

  </div>

  <div class="main_subtitle">Des menaces pour les enfants ?
  </div>
  <div class="content">
    <strong>Une menace psychologique :</strong><br>
    Le métier de youtubeur est basé sur la notoriété, c’est-à-dire que plus un youtubeur est connu, plus il « fera des vues » donc plus il touchera d’argent grâce à ses vidéos. La notoriété implique que beaucoup de gens regardent les vidéos, et il est fort peu probable que tout le monde apprécie le contenu, ou le youtubeur. <br>
    Les comptes YouTube étant des comptes anonyme, il n’est pas rare d’assister à un déchainement de violence, de haine et d’insultes à l’encontre du vidéaste. Ces commentaires négatifs peuvent être motivés par plusieurs choses : de la jalousie, du mécontentement, une haine simple du youtubeur, ...<br>
    La plupart des youtubeurs adultes chevronnés ont pris l’habitude de recevoir ce genre de commentaires, et ils ont une maturité suffisante pour passer outre et ne pas être blessés. En revanche, des enfants peuvent ne pas avoir le recul nécessaire, et, soumis à un déferlement de commentaires négatifs, peuvent être affectés psychologiquement. C’est un danger non négligeable qui peut être cause de dépressions, de trouble psychologiques, ou pire encore.<br>
    <br>
    <strong>Une menace sexuelle :</strong><br>
    Certaines rumeurs inquiétantes de pédophilie sont apparues récemment sur YouTube. Ces rumeurs sont venues de l’existence sur YouTube de supposés réseaux pédophiles, organisés à travers des chaines diffusant des vidéos suggestives d’enfants sur la plateforme.<br> 
    Dès lors, que penser des enfants mis en avant sur des chaines comptant des millions d’abonnés ? Ils peuvent très facilement devenir des cibles pour des pédophiles présents sur YouTube. C’est un danger potentiel lors des grosses rencontres avec les abonnés, comme par exemple la Paris Games Week.<br>

