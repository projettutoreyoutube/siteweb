    Polémiques liées à l'exploitation des mineurs
  </div>

  <div class="content">
    Sur YouTube, de nombreuses chaînes diffusent des vidéos mettant en avant des enfants. Les vidéos "déballage de cadeaux", "journée incroyable au parc d’attraction", "découverte du nouveau jouet playmobil", ... pullulent sur YouTube et envahissent les classements de tendances de la plateforme.
