    Des chaînes YouTube font l’apologie de la violence
  </div>
  <div class="content">
    Comme nous l’avons expliqué dans la partie concernant les infox, la censure est très difficile à mettre en place sur YouTube. De ce fait, les dérives concernant les contenus sont nombreuses et de tous genres. Beaucoup de sujets sont filtrés (comme par exemple le sexe), mais pas à entièrement. Certaines personnes mal intentionnées profitent de cette faiblesse du système de filtration pour faire l’apologie de sujets néfastes. Le principal sujet incriminé est l’apologie de la violence.<br>
<br>
    Beaucoup de vidéos sur YouTube montrent des fais de violence, sous plusieurs formes. Cette violence est parfois maitrisée dans le cadre d’un sport, comme par exemple les vidéos des combats de MMA à l’UFC qui engrangent des millions de vues, mais elle est souvent banalisée : on trouve sur YouTube de nombreuses vidéos de violence et de cruauté sans aucune forme d’explication.<br> 
    Le plus inquiétant est l’existence sur YouTube de chaînes faisant de la « publicité » pour des groupes terroristes, véhiculant une image de violence et de haine.<br>
<br>
    À cause de ces contenus, des jeunes personnes peuvent être confrontés à un monde qu’elles ne connaissent pas, et, encore une fois, ces jeunes n’ont pas forcément le recul nécessaire pour comprendre que ce monde de violence n’est pas et ne doit pas être la réalité du monde. <br>

