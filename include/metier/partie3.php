    Le youtubeur et ses revenus
  </div>
  <div class="content">
    Comme nous l’avons déjà expliqué, le youtubeur gagne de l’argent grâce à la publicité qui passe avant ou pendant la vidéo.<br />
    Nous vous conseillons d’aller voir la vidéo du « Rire Jaune » intitulée « L’argent sur YouTube » qui est très explicative et complète, et qui sert de base et de source à cette partie (le lien est en bas de page).<br />
    <br />
    La question qui nous intéresse donc dans cette partie est : combien gagne un youtubeur ?<br />
    Il est extrêmement difficile de répondre à cette question car beaucoup de facteurs entrent en ligne de compte. De plus, beaucoup d’informations sont gardées « secrètes » ou sont inexistantes.<br />
    Pour gagner de l’argent, un youtubeur doit être détenteur de la totalité des droits sur sa vidéo. Une fois que la propriété des droits a été vérifiée par YouTube, la vidéo peut être monétisée.<br />
    <br />
    Une vidéo monétisée rapporte en valeur brute 1$ pour 1000 vues, soit au 29/11/2018 : 0,88€.<br />
    Pour entrer dans ce compte, une publicité doit passer devant la vidéo, c’est-à-dire que toutes les « vues » sans publicité(s) sont retirés du décompte monétaire.<br />
    <br />
    Nous allons maintenant essayer de faire un exemple avec un youtubeur du niveau de Squeezie que nous appellerons Pierre. <br />
    « Essayer » car il est difficile de savoir exactement quelles sont les taxes qui pèsent sur les youtubeurs, ainsi que leurs dépenses, de plus, Squeezie profite d’avantages particuliers de YouTube, dus à son ancienneté sur la plateforme et à son nombre d’abonnés, et d’un contrat avec Webedia dont nous ne connaissons pas tous les aboutissant. Le site SocialBlade estime ses revenus publicitaires entre 210 000 et 3 380 000 euros par an, ce qui donne une énorme fourchette de prix.<br />
    <br />
    Au cours du mois d’octobre 2018, la chaine de Pierre a généré environ 33 millions de vues.<br />
    Depuis la vidéo réalisée par le « Rire Jaune » en 2015, YouTube a augmenté le nombre de publicités sur la plateforme et a réussi à « forcer » un maximum les publicités devant les vidéos. Les chiffres de YouTube Brandcast (les statistiques fournies par YouTube) annoncent qu’en 2017, 95% des vidéos étaient soumises à la publicité.<br />
    <br />
    Supposons que ces chiffres soient exacts, nous avons le calcul suivant :<br />
    33 000 000 x 95% = 31 350 000 vues<br />
    31 350 000 / 1000 x 1 dollar = 31 350 dollars<br />
    31 350 dollars = 27 588 euros<br />
    <br />
    La chaine de Pierre a donc généré 27 588 euros bruts en octobre. Viennent ensuite les taxes. <br />
    Un youtubeur générant ce niveau de revenu doit être déclaré en tant qu’entrepreneur (et non plus en tant qu’auto-entrepreneur). <br />
    Les charges sociétales sont alors au minimum de 50% et peuvent monter jusqu’à plus de 65%. Nous prendrons ici un taux de 55%.<br />
    <br />
    Il reste donc 12 414€ de revenus générés pour l’entreprise de Pierre. Si Pierre utilise du contenu protégé par des droits d’auteurs dans ses vidéos, il devra payer une part à un network pour en être protégé (dans notre cas ce sera 15%).<br />
    Après versement du pourcentage du network, il reste environ 10 550 euros dans les caisses de l’entreprise de Pierre. <br />
    <br />
    Pierre doit procéder à des investissements pour que sa chaîne reste compétitive, de plus il doit régler ses frais de fonctionnement. Planchons ici sur un coût de 10%.<br />
    Il reste alors un peu moins de 9500 euros pour l’entreprise de Pierre.<br />
    <br />
    L’entreprise de Pierre peut alors verser des salaires sur lesquels il devra payer des charges. Dans le cas d’une grosse chaine YouTube, il peut avoir des employés (cadreur, monteur) avoir des frais comptables, des frais d’avocats, …<br />
    <br />
    Supposons que Pierre fasse tout le travail lui-même. Il décide de se verser un salaire de 3000€ et de laisser 4500€ euros à sa société pour des potentiels besoins futurs et les charges patronales. Nous ignorons à quel pourcentage ces charges se situent dans ce cadre, nous laissons donc 150% du salaire versé sur le compte de l’entreprise<br />
    <br />
    3000€ par mois reviennent à 36 000 € par an. Une fois passés les impôts sur le revenu (qui sont de 30% dans cette tranche de revenus), il restera 25 200 euros soit 2100 euros par mois à Pierre.<br />
    <br />
    La question des revenus d’un youtubeur reste extrêmement floue. Elle s’éclaircira sans doute lorsque l’Etat créera un statut pour les youtubeurs, ce qu’il fera certainement pour pouvoir fixer des nouvelles taxes.<br />

