    Le youtubeur face à la loi
  </div>
  <div class="content">
    Cette page traite exclusivement des youtubeurs français. Toutes les notions sont donc rapportées au droit français.<br/>
    Un youtubeur a une activité nécessitant de l’expérience et rapportant un revenu. Le problème pour entrer dans la définition d’un métier, donnée plus haut, se situerai donc dans l’aspect légal de l’activité.
  </div>
  <div class="main_subtitle">
    Pourquoi déclarer son activité ?
  </div>
  <div class="content">
    Pour percevoir un revenu (légal), il faut exercer une activité professionnelle déclarée. En France, tout revenu doit être déclaré à l’Etat pour être soumis à des taxes.<br />
   Les impôts permettent de financer les institutions de l’Etat. Ensuite l’Etat réinvestit cet argent pour « améliorer » le pays. Ce réinvestissement se répercute sur le quotidiens des français qu’il est censé améliorer.<br />
   Dans la vie de tous les jours, cela peut, par exemple, être la sécurité sociale, ou la caisse des retraites.
  </div>
  <div class="main_subtitle">
    Que déclarer quand on est youtubeur ?
  </div>
  <div class="content">
    La difficulté réside dans la déclaration de cette activité : comment un youtubeur fait-il pour se déclarer ?<br />
    Il n’est fait mention nulle part, dans le droit français, d’une activité intitulée « youtubeur ».<br />
    En effet, la loi est très floue en ce qui concerne l’activité de youtubeur. Les youtubeurs ne sont pas directement des employés de YouTube au sens propre, bien que certains aient des contrats avec YouTube et que l’argent de la monétisation de vidéos soit donné par la plateforme. Il est donc nécessaire de déclarer son activité autrement. <br />

    Dans l’une de ses vidéos (intitulée « Être youtubeur »), Cyprien donne sa réponse à la question : qu’est-ce qu’un youtubeur ? Il déclare : « [...] (un youtubeur est) auteur, comédien, réalisateur, monteur, producteur de vidéos sur internet. ». <br />
    Or, chacune des qualifications qu’il cite est reconnue comme étant une vraie profession qu’il est possible de déclarer, il devrait donc être logique que l’activité « youtubeur » soit référencée par la loi.<br />

    Comme ce n’est pas le cas, un youtubeur générant des revenus doit se déclarer avec le statut d’auto-entrepreneur, et même créer une entreprise s’il dépasse un certain stade de chiffre d’affaire (32 800€ par an) ou s’il s’associe avec d’autres youtubeurs (par exemple : la RedBox).<br />
    Le statut d’auto-entrepreneur est assez simple à obtenir, et en l’obtenant, le youtubeur pourra être imposé sur ses revenus. Le plus gros avantage est qu’il pourra cotiser pour une future retraite, ce qui est impossible pour lui autrement. Un auto-entrepreneur est taxé aux environs de 23% des revenus qu’il génère.<br />

    Pour éviter ces démarches administratives, qui peuvent devenir fastidieuses, un youtubeur a la possibilité de « vendre » sa chaîne. C’est le cas par exemple de McFly & Carlito qui ont vendu leur chaîne à Webedia. Dans les faits, l’argent généré par la chaîne va à Webedia, qui reverse ensuite un pourcentage en salaire à McFly & Carlito. En échange de la cession de leur chaîne, Webedia gère tout l’administratif pour eux, et leur offre contacts, matériel et locaux.

